package es.ua.dlsi.im3.omr.classifiers.traced;

/**
 * @deprecated Use package es.ua.dlsi.im3.omr.classifiers.symbolrecognition
 */
public class Coordinate {
	double x;
	double y;
	public Coordinate(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	
}
